SELECT 
CASE
    WHEN PRICE>0 && PRICE<10000 THEN '0'
    WHEN PRICE>=10000 && PRICE<20000 THEN 10000
    WHEN PRICE>=20000 && PRICE<30000 THEN 20000
    WHEN PRICE>=30000 && PRICE<40000 THEN 30000
    WHEN PRICE>=40000 && PRICE<50000 THEN 40000
    WHEN PRICE>=50000 && PRICE<60000 THEN 50000
    WHEN PRICE>=60000 && PRICE<70000 THEN 60000
    WHEN PRICE>=70000 && PRICE<80000 THEN 70000
    WHEN PRICE>=80000 && PRICE<90000 THEN 80000
END AS PRICE_GROUP, COUNT(*) AS PRODUCTS
FROM PRODUCT
GROUP BY PRICE_GROUP
ORDER BY PRICE_GROUP;
